# 也可以为多个对象指定多个变量,注意等号前后个数不一致会报错
a, b, c = 1, 2, "runoob"

print(a)
print(b)
print(c)

# 内置的 type() 函数可以用来查询变量所指的对象类型
print(type(a))
print(type(c))

